import Foundation
import UIKit

func createTextField() -> UITextField {
    let tf = UITextField()
    return tf
}

func createTextView() -> UITextView {
    let tv = UITextView()
    tv.font = UIFont.systemFont(ofSize: 17)
    return tv
}
